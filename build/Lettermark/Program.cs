using System.Globalization;
using System.Text;
using SkiaSharp;

// Compose assets/icons/wiseowl-casc.svg: a clean "CASC" 2×2 lettermark
// from a bold typeface (glyph outlines baked to path data), on the shared
// Wise Owl tile, autumn-brown ink, owl brand tick, family keyline.
// Output is deterministic for consumers (the SVG carries the vector path;
// no font is needed to render it).

var outPath = args.Length > 0 ? args[0] : "assets/icons/wiseowl-casc.svg";

// A heavy face reads as an icon; fall back gracefully. The chosen font
// only matters on the build machine — the SVG bakes the outlines.
string[] families = ["Arial Black", "Franklin Gothic Heavy", "Impact",
                     "Segoe UI Black", "Arial"];
SKTypeface face = null;
foreach (var f in families)
{
    var tf = SKTypeface.FromFamilyName(f, SKFontStyleWeight.ExtraBold,
        SKFontStyleWidth.Normal, SKFontStyleSlant.Upright);
    if (tf != null && tf.FamilyName.Contains(f.Split(' ')[0],
            StringComparison.OrdinalIgnoreCase)) { face = tf; break; }
    tf?.Dispose();
}
face ??= SKTypeface.Default;
Console.WriteLine($"font: {face.FamilyName}");

using var paint = new SKPaint { Typeface = face, TextSize = 256, IsAntialias = true };

// 2×2 cells (room left for the owl tick + keyline).
(string Ch, float Cx, float Cy)[] cells =
[
    ("C", 86, 90), ("A", 170, 90),
    ("S", 86, 156), ("C", 170, 156),
];
const float cellH = 70f;

var letterPaths = new List<string>();
foreach (var (ch, cx, cy) in cells)
{
    using var gp = paint.GetTextPath(ch, 0, 0);
    var b = gp.TightBounds;
    var s = cellH / b.Height;
    var m = SKMatrix.CreateIdentity();
    m = m.PostConcat(SKMatrix.CreateTranslation(-b.MidX, -b.MidY));
    m = m.PostConcat(SKMatrix.CreateScale(s, s));
    m = m.PostConcat(SKMatrix.CreateTranslation(cx, cy));
    gp.Transform(m);
    letterPaths.Add(gp.ToSvgPathData());
}

var sb = new StringBuilder();
void L(string s) => sb.Append(s).Append('\n');
L("<?xml version=\"1.0\" encoding=\"UTF-8\"?>");
L("<!-- WiseOwl.Casc mark: bold \"CASC\" lettermark (typeface glyph");
L("     outlines baked to vector — no runtime font dependency), 2×2 on");
L("     the shared Wise Owl tile, autumn-brown ink, owl brand tick,");
L("     family keyline. Sibling of the D4 \"D·IV\" mark. Generated by");
L("     build/Lettermark; do not hand-edit. -->");
L("<svg xmlns=\"http://www.w3.org/2000/svg\" viewBox=\"0 0 256 256\" width=\"256\" height=\"256\">");
L("  <defs>");
L("    <linearGradient id=\"tileC\" x1=\"0\" y1=\"0\" x2=\"0\" y2=\"1\">");
L("      <stop offset=\"0\" stop-color=\"#161b22\"/><stop offset=\"1\" stop-color=\"#0b0e13\"/></linearGradient>");
L("    <linearGradient id=\"ink\" x1=\"0\" y1=\"0\" x2=\"0\" y2=\"1\">");
L("      <stop offset=\"0\" stop-color=\"#A8743A\"/><stop offset=\"1\" stop-color=\"#8A5A33\"/></linearGradient>");
L("    <radialGradient id=\"warm\" cx=\"0.5\" cy=\"0.45\" r=\"0.6\">");
L("      <stop offset=\"0\" stop-color=\"#A8743A\" stop-opacity=\"0.12\"/><stop offset=\"1\" stop-color=\"#A8743A\" stop-opacity=\"0\"/></radialGradient>");
L("  </defs>");
L("  <rect width=\"256\" height=\"256\" rx=\"52\" fill=\"url(#tileC)\"/>");
L("  <ellipse cx=\"128\" cy=\"118\" rx=\"96\" ry=\"92\" fill=\"url(#warm)\"/>");
foreach (var d in letterPaths)
    L($"  <path fill=\"url(#ink)\" d=\"{d}\"/>");
// small Wise Owl brand tick (signature) beneath the lettermark
L("  <g fill=\"#8A5A33\">");
L("    <path fill=\"none\" stroke=\"#8A5A33\" stroke-width=\"4\" stroke-linecap=\"round\" d=\"M114 200 L122 207 M142 200 L134 207\"/>");
L("    <circle cx=\"121\" cy=\"211\" r=\"4.5\"/><circle cx=\"135\" cy=\"211\" r=\"4.5\"/>");
L("    <path d=\"M128 214 L132 221 L124 221 Z\"/>");
L("  </g>");
L("  <rect x=\"10\" y=\"10\" width=\"236\" height=\"236\" rx=\"44\" fill=\"none\" stroke=\"#8A5A33\" stroke-width=\"3\" opacity=\"0.55\"/>");
L("</svg>");
File.WriteAllText(outPath, sb.ToString());
Console.WriteLine($"wrote {outPath}");
